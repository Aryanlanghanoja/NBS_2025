#include <iostream>
using namespace std;

class Solution
{
public:
    void isInRange(int n)
    {
        // you don't need to take input
        // here just write code which print correct answer according to n
        switch (n)
        {
        case 1:
            cout << "one\n";
            break;
        case 2:
            cout << "two";
            break;
        case 3:
            cout << "three\n";
            break;
        case 4:
            cout << "four\n";
            break;
        case 5:
            cout << "five\n";
            break;
        case 6:
            cout << "six\n";
            break;
        case 7:
            cout << "seven\n";
            break;
        case 8:
            cout << "eight\n";
            break;
        case 9:
            cout << "nine\n";
            break;
        case 10:
            cout << "ten\n";
            break;
        default:
            cout << "not in range\n";
            break;
        }
    }
};

int main()
{
    Solution solution;
    int n;
    cout << "Enter a number between 1 and 10: ";
    cin >> n;
    solution.isInRange(n);
    return 0;
}